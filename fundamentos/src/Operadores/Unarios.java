package operadores;

public class Unarios {
    public static void main(String[] args) {
        // Declaração de variáveis
        int a = 1;
        int b = 2;
        
        // Operador de incremento
        System.out.println(a++); // 1
        System.out.println(a); // 2
        
        // Operador de decremento
        System.out.println(--b); // 1
        System.out.println(b); // 1
    }
}
